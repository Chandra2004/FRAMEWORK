<?php

namespace TheFramework\App\Database\Casts;

use TheFramework\App\Database\Model;

/**
 * Interface for custom attribute casters.
 */
interface CastsAttributes
{
    /**
     * Transform the attribute from the underlying model value.
     *
     * @param  Model  $model
     * @param  string  $key
     * @param  mixed  $value
     * @param  array  $attributes
     * @return mixed
     */
    public function get($model, string $key, $value, array $attributes);

    /**
     * Transform the attribute to the underlying model value.
     *
     * @param  Model  $model
     * @param  string  $key
     * @param  mixed  $value
     * @param  array  $attributes
     * @return mixed
     */
    public function set($model, string $key, $value, array $attributes);
}
